const std = @import("std");
const editor = @import("editor.zig");
const buffer = @import("buffer.zig");

pub fn main() !void {
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer switch (gpa.deinit()) {
        .leak => std.debug.panic("Some memory leaked!", .{}),
        .ok => {},
    };

    const ed = try editor.Editor.init(gpa.allocator());
    defer ed.deinit();

    if (std.os.argv.len > 1) {
        for (std.os.argv[1..]) |fname| {
            try ed.add_buffer(std.mem.span(fname));
        }
    }

    while (!ed.quit_flag) {
        try ed.refreshScreen();
        const c = try ed.readKey();
        try ed.processKeypress(c);
    }
}
